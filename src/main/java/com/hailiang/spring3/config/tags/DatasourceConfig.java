package com.hailiang.spring3.config.tags;

public class DatasourceConfig {
	private String dbname;
	private String datasouceip;
	private String username;
	private String password;
	private String characterEncoding;
	private String configLocation;
	
	private String dbname123;

	public String getDbname() {
		return dbname;
	}

	public void setDbname(String dbname) {
		this.dbname = dbname;
	}

	public String getDatasouceip() {
		return datasouceip;
	}

	public void setDatasouceip(String datasouceip) {
		this.datasouceip = datasouceip;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCharacterEncoding() {
		return characterEncoding;
	}

	public void setCharacterEncoding(String characterEncoding) {
		this.characterEncoding = characterEncoding;
	}

	public String getConfigLocation() {
		return configLocation;
	}

	public void setConfigLocation(String configLocation) {
		this.configLocation = configLocation;
	}

	public String getDbname123() {
		return dbname123;
	}

	public void setDbname123(String dbname123) {
		this.dbname123 = dbname123;
	}
	
}
